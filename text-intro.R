text_intro <- "\\chapter{Informe Test Àtom}


Amb l'objectiu d'apropar els avenços científics a la realitat escolar, el Projecte Òrbita, equip de recerca multidisciplinari en educació  infantil, ha elaborat el Test Àtom.

El Test Àtom és una bateria d'ampli espectre de proves de rendiment escolar, d'habilitats cognitives i d'adaptació  emocional. Aquesta prova ens permet conèixer el perfil cognitiu de cada alumne per tal de poder oferir o recomanar la intervenció personal i acadèmica més adequada a cada persona. Comprenent el seu perfil d'aprenentatge i comparant el rendiment de cada alumne amb el seu grup-classe i amb la població  general del seu curs, comprendrem la seva adaptació  i podrem facilitar als mestres els recursos necessaris per gestionar la complexitat de cada aula. Pensem que la diversitat enriqueix; per això, des del Projecte Òrbita reivindiquem la diferència destinant eines per la detecció  de les diferències d'aprenentatge i connectant les escoles amb la recerca científica en educació  i psicologia infantil.

Fruit de la col·laboració  de la vostra escola amb el Projecte Òrbita, trobareu a continuació  els resultats obtinguts pels alumnes.


\\section{Introducció  Test Àtom}

El Test Àtom, desenvolupat pel Projecte Òrbita, és una bateria d'ampli espectre que avalua el perfil cognitiu dels alumnes. Suposa una mesura objectiva, integradora i científicament validada del rendiment acadèmic, les habilitats cognitives i l'adaptació  emocional a través de les àrees de \\textbf{lectura}, \\textbf{fluïdesa matemàtica}, \\textbf{raonament lògic}, \\textbf{velocitat de processament}, \\textbf{memòria de treball i a llarg termini} i \\textbf{desenvolupament emocional}.

\\section{Interpretació  de l'informe}

Aquestes dades s'analitzen i interpreten en dues dimensions principals:

\\begin{itemize}
\\item Col·lectiva: obtenció  del perfil intern del grup-classe, que permet oferir una \\textit{fotografia} completa de les aptituds dels alumnes de cada aula i la comparació  amb la població  general (barem universal).

\\item Individual: obtenció  del perfil individual de cada alumne en habilitats cognitives i adaptació  emocional, amb l'objectiu de comprendre les seves fortaleses i dificultats i la seva comparació  amb la població  general.



Cada informe consta de la informació  referent a 4 aspectes:


\\begin{enumerate}
\\item Valoració  cognitiva i de rendiment escolar: Informa sobre la capacitat i l'execució  de l'alumne en 6 o 7 àrees cognitives: Lectura, Memòria de Treball, Velocitat de Processament, Fluïdesa Matemàtica, Memòria a Llarg Termini, Raonament, (Càlcul).

\\item Índex d'estil de resposta (Reflexivitat-Impulsivitat): Aporta informació  sobre si el patró  de resposta del subjecte és impulsiu o reflexiu i permet matisar les inferències sobre el seu rendiment, situant cada alumne en un punt intermig en el continuum reflexivitat-impulsivitat.

\\item Valoració  adaptativa: Percepció  subjectiva de l'alumne sobre la seva adaptació  a nivell Personal, Social, Escolar i Familiar.

\\item Orientacions personals i educatives: Recomanacions en funció  de la informació  obtinguda sobre l'abordatge de les dificultats i potencialitats constatades.
\\end{enumerate}
\\end{itemize}


\\section{Mètode Òrbita d'anàlisi estadístic}

El Test Àtom es basa en mesurar la velocitat d'execució  de les diferents tasques per tal de calcular l'habilitat en cada una d'elles. D'aquesta manera podem dibuixar la distribució  d'habilitats de tots els nens i nenes a través d'una prova de passació  col·lectiva. És a dir, traslladem la tradicional passació  individual en la que sovint es mesura quant es tarda a solucionar un cert número fixe d'ítems a fer-ho a la inversa, i veure quants ítems es poden respondre en un temps concret. 



Això fa que, evidentment, la velocitat de processament tingui un pes molt important en tots els resultats. Per tal d'obtenir l'informació  més acurada i isolada possible sobre cada paràmetre d'estudi és important treure aquest efecte. És per això que hem desenvolupat el Mètode Òrbita d'anàlisi estadístic dels resultats\\footnote{Aquest càlcul no es duu a terme en la memòria de treball ni en la memòria a llarg termini, ja que aquestes no depenen de la velocitat de processament.}. 



Aquest mètode estadístic consisteix en tres passos:


\\begin{enumerate} 

\\item Una predicció  del que anomenem l'\\emph{índex de rapidesa (IR)}, que equivaldria a una velocitat de processament base del nen o nena, calculada a través d'extreure la informació  de la velocitat d'execució  de les diferents proves. 
\\item La utilització  d'aquest índex de rapidesa per tal de predir els resultats de les diferents proves prenent com a model tots els resultats de tests obtinguts a òrbita. 
\\item La mesura de la diferència entre el valor mesurat i el predit segons la velocitat de processament. 

\\end{enumerate} 



Per tant, aquesta diferència ens indica possibles àrees on hi hagi una dificultat o una potencialitat extraordinàries. És a dir, mitjançant aquest anàlisi detectem aquells àmbits on el rendiment és inferior (o molt superior) al predit segons l'índex de rapidesa, i per tant só n aquelles àrees on pot ser més eficient un treball específic de millora. 
És a dir, els valors predits per l'alumne só n aquells que tindria un perfil 'estàndard' amb les mateixes habilitats que l'alumne estudiat. Si veiem que el perfil real mesurat és significativament diferent que l'estàndard, parlem d'una dificultat (o talent) específica.


Per il·lustrar aquest fet mostrem en els informes individuals dos gràfics, el que anomenem experimental i el predit. En l'experimental mostrem els resultats tal com els hem mesurat amb el test àtom. El codi de colors és el següent: 


\\begin{enumerate}[label=\\alph*.]
\\item Taronja: rendiment per sota del 30\\% de la població .
\\item Verd: rendiment dins la normalitat.
\\item Blau: rendiment per sobre del 85\\% de la població . 
\\end{enumerate} 


En el segon gràfic mostrem els resultats predits pel Mètode Òrbita, i els coloregem segons la diferència amb els originals. El codi de colors és el següent: 

\\begin{enumerate}[label=\\alph*.]
\\item Vermell: el valor predit és significativament (una desviació  estàndard) més alt que el mesurat, i per tant detectem una dificultat en aquella àrea. 
\\item Gris: el valor predit és similar al mesurat,
\\item Blau: el valor predit és significativament (una desviació  estàndard) més baix que el mesurat, i per tant detectem una potencialitat. 
\\end{enumerate} 

