\\section{Orientacions}

Les proves que hem passat a la vostra escola formen part de la primera fase (a la que anomenem Tier I) del Test Àtom. Com hem explicat anteriorment, aquesta fase avalua el rendiment escolar, l'adaptació  emocional i les funcions executives. Els resultats d'aquestes avaluacions ens indiquen en quines àrees generals hi pot haver necessitats específiques d'aprenentatge.  Segons aquests resultats es configura un Tier II adaptat a les característiques de cada perfil que hem observat en el Tier I. 

Per a cada nen o nena, doncs, redactem les conclusions del test i com aquestes suggereixen quin tipus de proves administrar a partir d'ara i quins passos a seguir en un futur. Encara que el nostre Tier II no està acabat i per tant no es pot administrar, amb aquesta informació  volem donar la oportunitat a aquells que han passat el Test Àtom en aquesta fase de baremació  per continuar explorant el seu perfil congitiu d'una forma raonada i útil per tal de millorar el rendiment escolar. 

Les relacions entre les proves del Tier I i les del Tier II estan basades en el coneixement científic actual sobre habilitats cognitives, funcions executives, desenvolupament emocional i habilitats socials i adaptatives. Adjunt en aquest document trobareu l'explicació  de quines só n les teories que ho recolzen i configuren la base científica del Test Àtom." 
